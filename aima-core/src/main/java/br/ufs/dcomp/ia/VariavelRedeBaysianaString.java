package br.ufs.dcomp.ia;

public class VariavelRedeBaysianaString {
	private String renda;
	private String bolsa;
	
	public VariavelRedeBaysianaString(String renda, String bolsa) {
		this.renda = renda;
		this.bolsa = bolsa;
	}
	
	public String getRenda() {
		return renda;
	}

	public void setRenda(String renda) {
		this.renda = renda;
	}

	public String getBolsa() {
		return bolsa;
	}

	public void setBolsa(String bolsa) {
		this.bolsa = bolsa;
	}

}
